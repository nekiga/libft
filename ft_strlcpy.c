#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (i++ != size -1)
		dst[i] = src[i];
	dst[i +1] = '\0';
	return (i);
}
